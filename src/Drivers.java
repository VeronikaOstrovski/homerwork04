public class Drivers {

    String gender;
    int age;
    int drivingExperience;

    public int incrementByOneAge(int age) {
        age ++;
        return age;
    }

    public int incrementByOneDrivingExperience(int drivingExperience) {
        drivingExperience ++;
        return drivingExperience;
    }

    public int decrementByOneAge(int age) {
        age --;
        return age;
    }

    public int decrementByOneDrivingExperience(int drivingExperience) {
        drivingExperience --;
        return drivingExperience;
    }

}


